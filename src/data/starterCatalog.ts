import type { RequirementRow, TaskGraph } from "../types";

const ROB = "Ring of Brodgar";

type RowDraft = Omit<RequirementRow, "source">;

function row(draft: RowDraft): RequirementRow {
  return {
    ...draft,
    source: ROB,
  };
}

function outlineTask(task: Omit<TaskGraph, "rows"> & { rows: RowDraft[] }): TaskGraph {
  return {
    ...task,
    rows: task.rows.map(row),
  };
}

